// Figura 2.4: Welcome3.java
// Imprimindo Multiplas linhas de texto com uma única instrução.

public class Welcome3 {

    // método main inicia a excecução do aplicativo Java
    public static void main(String[] args) {

        System.out.println("Welcome\nto\nJava\nProgramming!");
        System.out.println("\"entre aspas\"");

    } // fim do método main
}   // fim da classe welcome3


/*

\n   Nova linha. Posisiona o cursor de tela no início da próxima linha.

\t   Tabulação horizontal. Move o cursor de tela para próxima parada de tabulação.

\r   Retorno de carro. Posiciona o cursor da tela no início da linha atual - não avança para a próxima linha.
Qualquer saída de caracteres depois do retorno de carro sobreescreve a saída de caracteres ateriormente gerada na linha atual.

\\ Barra invertida. Utilizadas para imprimir um caractere de barra invetida.

\" Aspas duplas. Utilizada para imprimir um caractere de aspas duplas. Por exemplo,
    System.out.println("\" entre aspas\"");
    exibe " entre aspas".

 */
