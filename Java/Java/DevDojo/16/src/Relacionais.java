

public class Relacionais {

    public static void main(String[] args) {

        int sum1 = 10;
        int sum2  = 20;
        double result = sum1 / (double) sum2;

        System.out.println(result);

        int resto = 21 % 7;
        System.out.println(resto);

        boolean comparaMaiorQueVinte = 10 > 20;
        boolean comparaMenorQueVinte = 10 < 20;
        boolean igual = 10 == 20;
        boolean diferente = 10 != 20;
        System.out.println(comparaMaiorQueVinte);
        System.out.println(comparaMenorQueVinte);
        System.out.println(igual);
        System.out.println(diferente);

    }   
}
